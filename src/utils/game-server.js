export function findServerInfo(serverName) {
    var tmpServerName = serverName;
    const results = [];

    gameServerData.forEach(function (phase) {
        const servers = phase.servers;
        var found = false;
        var mainServerName = null;

        const phaseResults = [];

        Object.keys(servers).forEach(function (mainServer) {
            const subServers = servers[mainServer];
            if (mainServer === serverName
                || subServers.indexOf(serverName) !== -1
                || subServers.indexOf(mainServerName) !== -1) {
                phaseResults.push([mainServer, subServers]);
                found = true;
                mainServerName = mainServer;
            }
        });

        if (found && mainServerName) {
            serverName = mainServerName;
            const greatWorld = phase.greatWorld;
            Object.keys(greatWorld).forEach(function (gw) {
                if (greatWorld[gw].indexOf(mainServerName) !== -1) {
                    phaseResults.push([gw]);
                }
            })
        }

        if (phaseResults.length > 0) results.push(phaseResults);
    });

    return results;
}

export function getServerList() {
    const results = {};

    gameServerData.forEach(function (phase) {
        const servers = phase.servers;
        Object.keys(servers).forEach(function (mainServer) {
            results[mainServer] = "";
            servers[mainServer].forEach(function (subServer) {
                results[subServer] = "";
            })
        })
    })

    return Object.keys(results);

}

